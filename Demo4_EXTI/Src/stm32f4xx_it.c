/** 
 * @brief 外部中断回调函数 
 * @note  在HAL_GPIO_EXTI_IRQHandler()函数中自动调用
 *             WKUP->PA0
 *             key0->PH3
 *             key1->PH2
 *             key2->PC13
 * @param {uint16_t} GPIO_Pin
 * @retval 无
 */
void HAL_GPIO_EXTI_Callback(uint16_t GPIO_Pin)
{
    delay_ms(100); //消抖
    switch (GPIO_Pin)
    {
    case GPIO_PIN_0:
        if (WKUP == 1) //延时后依然按下
        {
            LED1 = LED0;
            LED0 = !LED0;
        }
        break;
    case GPIO_PIN_2:
        if (KEY1 == 0)
        {
            LED1 = !LED1;
        }
        break;
    case GPIO_PIN_3:
        if (KEY0 == 0)
        {
            LED1 = !LED1;
            LED0 = LED1;
        }
        break;
    case GPIO_PIN_13:
        if (KEY2 == 0)
        {
            LED0 = !LED0;
        }
        break;
    }
}

